package TPO.LecturaYDevolucion;

public class TripulacionesOrigen {
    private int CantTripulaciones;
    private String Origen;

    public void setCantTripulaciones(int tripulaciones) {
        CantTripulaciones = tripulaciones;
    }

    public int getCantTripulaciones() {
        return CantTripulaciones;
    }

    public String getOrigen() {
        return Origen;
    }

    public void setOrigen(String origen) {
        Origen = origen;
    }
}
