package TPO;

public class TripulacionesOrigen {
    private int Tripulaciones;
    private String Origen;

    public void setTripulaciones(int tripulaciones) {
        Tripulaciones = tripulaciones;
    }

    public int getTripulaciones() {
        return Tripulaciones;
    }

    public String getOrigen() {
        return Origen;
    }

    public void setOrigen(String origen) {
        Origen = origen;
    }
}
